package com.catchdog.login.vo;

public class TempPwVO {

	private String pnum;
	private String ptemppw;
	private String mid;
	private String mnum;
	private String insertdate;

	public TempPwVO() {
	}

	public TempPwVO(String pnum, String ptemppw, String mid, String mnum, String insertdate) {
		this.pnum = pnum;
		this.ptemppw = ptemppw;
		this.mid = mid;
		this.mnum = mnum;
		this.insertdate = insertdate;
	}

	public String getPnum() {
		return pnum;
	}

	public String getPtemppw() {
		return ptemppw;
	}

	public String getMid() {
		return mid;
	}
	
	public String getMnum() {
		return mnum;
	}

	public String getInsertdate() {
		return insertdate;
	}

	public void setPnum(String pnum) {
		this.pnum = pnum;
	}

	public void setPtemppw(String ptemppw) {
		this.ptemppw = ptemppw;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public void setMnum(String mnum) {
		this.mnum = mnum;
	}
	
	public void setInsertdate(String insertdate) {
		this.insertdate = insertdate;
	}
}
